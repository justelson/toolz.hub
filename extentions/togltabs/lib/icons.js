export function icon(key, size = 16) {
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${ICONS[key] || ''}</svg>`;
}

export function brandIcon(site, size = 16) {
  const brands = {
    youtube: { slug: 'youtube', color: 'FF0000' },
    instagram: { slug: 'instagram', color: 'E1306C' },
    x: { slug: 'x', color: 'ffffff' }
  };
  const brand = brands[site];
  if (!brand) return null;
  return `<img src="https://cdn.simpleicons.org/${brand.slug}/${brand.color}" width="${size}" height="${size}" alt="${site}" />`;
}

export function textFallback(label, color, size = 24) {
  const initials = (label || '?').slice(0, 2).toUpperCase();
  return `<div style="width:${size}px;height:${size}px;border-radius:4px;background:${color}20;color:${color};display:flex;align-items:center;justify-content:center;font-family:var(--font-mono);font-size:10px;font-weight:600;">${initials}</div>`;
}
